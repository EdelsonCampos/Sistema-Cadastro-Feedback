    private DefaultTableModel montarTabela() {
    String[] colunas = {"id", "Funcionario", "Registro", "Cargo"};

    // Criamos a tabela
    DefaultTableModel tabela = new DefaultTableModel(colunas, 0);

    List<Cadastro> lista = ProdutoDAO.listarTodos();

    for (Cadastro p : lista) {
        String[] linha = {
            Integer.toString(p.getId()),
            p.getFuncionario(), // Corrigido para getFuncionario
            Cadastro.converterParaJava(p.getRegistro()), // Corrigido para LocalDate
            p.getCargo() // Corrigido para getCargo
        };

        tabela.addRow(linha);
    }

    return tabela;
    }

    private int getPosicao() {
        int posicao = tabela.getSelectedRow();
        if (posicao <= -1) {
            JOptionPane.showMessageDialog(null, "Selecione um item para excluir.");
        }
        return Integer.parseInt((String) tabela.getValueAt(posicao, 0));
    }

